    def _draw_waveform(self, waveform: np.ndarray) -> None:
        r       = self.wave_rect
        amp     = self._amp_slider.value
        mid_y   = r.centery
        draw_x0 = r.left + LABEL_PAD
        draw_w  = r.width - LABEL_PAD
        draw_h  = r.height - 20

        lbl = self.font_sm.render("WAVEFORM", True, WAVE_COL)
        self.screen.blit(lbl, (draw_x0, r.top + 5))

        for frac in (1.0, 0.5, 0.0, -0.5, -1.0):
            y = int(mid_y - frac * draw_h / 2)
            if not (r.top + 2 <= y <= r.bottom - 2):
                continue
            pygame.draw.line(self.screen, GRID, (draw_x0, y), (r.right, y))
            v    = frac / amp
            text = "0" if abs(v) < 1e-9 else f"{v:+.2g}"
            ls   = self.font_sm.render(text, True, GRID)
            self.screen.blit(ls, (r.left + 2, y - ls.get_height() // 2))

        pygame.draw.line(self.screen, ACCENT, (0, r.bottom), (self.W, r.bottom))

        # Trigger level line (yellow dashed) when trigger is active
        if self._trig_mode_idx != 0:
            trig_y = int(mid_y - self.trigger_level * amp * (draw_h / 2))
            trig_y = max(r.top + 2, min(r.bottom - 2, trig_y))
            # Draw dashed line
            dash_len = 10
            for dx in range(draw_x0, r.right, dash_len * 2):
                x_end = min(dx + dash_len, r.right)
                pygame.draw.line(self.screen, TRIG_COL, (dx, trig_y), (x_end, trig_y), 1)
            # Small triangle marker on left edge
            tri_x = draw_x0 - 2
            pygame.draw.polygon(self.screen, TRIG_COL, [
                (tri_x, trig_y),
                (tri_x - 10, trig_y - 6),
                (tri_x - 10, trig_y + 6),
            ])
            # Mode label top-right of waveform panel
            mode_lbl = self.font_sm.render(f"TRIG: {self.trigger_mode}", True, TRIG_COL)
            self.screen.blit(mode_lbl, (r.right - mode_lbl.get_width() - 10, r.top + 5))

        if len(waveform) < 2:
            return

        n  = len(waveform)
        xs = np.linspace(draw_x0, r.right - 1, n).astype(int)
        ys = (mid_y - waveform * amp * (draw_h / 2)).astype(int)
        ys = np.clip(ys, r.top + 2, r.bottom - 2)
        pygame.draw.lines(self.screen, WAVE_COL, False,
                          list(zip(xs.tolist(), ys.tolist())), 2)

    def _draw_fft(self, fft_db: np.ndarray, freqs: np.ndarray) -> None:
        r        = self.fft_rect
        max_freq = max(200, self._freq_slider.int_value)
        draw_x0  = r.left + LABEL_PAD
        draw_w   = r.width - LABEL_PAD
        draw_bot = r.bottom - 22
        draw_h   = draw_bot - r.top - 26

        lbl = self.font_sm.render("SPECTRUM (FFT)", True, FFT_COL)
        self.screen.blit(lbl, (draw_x0, r.top + 5))

        def db_to_y(db: float) -> int:
            frac = (db - DB_MIN) / (DB_MAX - DB_MIN)
            return int(draw_bot - frac * draw_h)

        for db in (-60, -40, -20, 0):
            y = db_to_y(db)
            if r.top + 20 <= y <= draw_bot:
                pygame.draw.line(self.screen, GRID, (draw_x0, y), (r.right, y))
                ls = self.font_sm.render(f"{db:+d}", True, GRID)
                self.screen.blit(ls, (r.left + 2, y - ls.get_height() // 2))

        for f in FREQ_TICKS:
            if f > max_freq:
                break
            x = draw_x0 + int(f / max_freq * draw_w)
            pygame.draw.line(self.screen, GRID, (x, r.top + 24), (x, draw_bot))
            lbl_f = f"{f // 1000}k" if f >= 1000 else str(f)
            ls = self.font_sm.render(lbl_f, True, GRID)
            self.screen.blit(ls, (x - ls.get_width() // 2, draw_bot + 3))

        if len(fft_db) < 2 or len(freqs) < 2:
            return

        mask   = freqs <= max_freq
        f_sel  = freqs[mask]
        db_sel = np.clip(fft_db[mask], DB_MIN, DB_MAX)

        if len(f_sel) < 2:
            return

        xs = (draw_x0 + f_sel / max_freq * draw_w).astype(int)
        ys = np.array([db_to_y(float(d)) for d in db_sel], dtype=int)
        ys = np.clip(ys, r.top + 24, draw_bot)

        self._fill_surf.fill((0, 0, 0, 0))
        poly = ([(int(xs[0]),  draw_bot - r.top)]
                + [(int(x), int(y) - r.top) for x, y in zip(xs, ys)]
                + [(int(xs[-1]), draw_bot - r.top)])
        pygame.draw.polygon(self._fill_surf, (*FFT_COL, 45), poly)
        self.screen.blit(self._fill_surf, (0, r.top))

        pts = list(zip(xs.tolist(), ys.tolist()))
        if len(pts) >= 2:
            pygame.draw.lines(self.screen, FFT_COL, False, pts, 2)
